'use strict';

// chrome.storage API to store, retrieve, and track changes to user data.

chrome.runtime.onInstalled.addListener(function() {
    chrome.storage.sync.set({color: '#3aa757'}, function() {
      console.log('The color is green.');
    });
    chrome.declarativeContent.onPageChanged.removeRules(undefined, function() {
      chrome.declarativeContent.onPageChanged.addRules([{
        conditions: [new chrome.declarativeContent.PageStateMatcher({
          pageUrl: {hostEquals: 'developer.chrome.com'},
        })
        ],
            actions: [new chrome.declarativeContent.ShowPageAction()]
      }]);
    });
  });

// most APIs, like storage API need to add at permission at manifest file
// dont forget to add declarativeContent to the permission as well.
// declarativeContent.PageStateMatcher -> when a user navigate to developer.chrome.com
// now the gray icon will turn into colored icon.